const IconSelector = ({ selectedIcon, onChange }) => {
  const [isOpen, setIsOpen] = useState(false);

  const handleSelectIcon = (iconType) => {
    onChange(iconType);
    setIsOpen(false);
  };

  return (
    <div className="relative">
      <label className="block text-sm font-medium text-gray-700 mb-1">Button Icon</label>
      <div
        className="flex items-center justify-between p-2 border border-gray-300 rounded-md cursor-pointer hover:border-blue-500"
        onClick={() => setIsOpen(!isOpen)}
      >
        <div className="flex items-center">
          <div className="w-8 h-8 flex items-center justify-center bg-gray-100 rounded-md mr-2">
            {IconOptions[selectedIcon] || IconOptions.lightning}
          </div>
          <span className="text-sm capitalize">{selectedIcon}</span>
        </div>
        <svg
          xmlns="http://www.w3.org/2000/svg"
          className={`h-5 w-5 transition-transform ${isOpen ? 'transform rotate-180' : ''}`}
          fill="none"
          viewBox="0 0 24 24"
          stroke="currentColor"
        >
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
        </svg>
      </div>

      {isOpen && (
        <div className="absolute z-10 mt-1 w-full bg-white border border-gray-300 rounded-md shadow-lg">
          <div className="p-2 grid grid-cols-5 gap-2">
            {Object.entries(IconOptions).map(([iconType, icon]) => (
              <div
                key={iconType}
                className={`p-2 flex flex-col items-center justify-center rounded-md cursor-pointer hover:bg-blue-50 ${selectedIcon === iconType ? 'bg-blue-100' : ''}`}
                onClick={() => handleSelectIcon(iconType)}
              >
                <div className="w-8 h-8 flex items-center justify-center">
                  {icon}
                </div>
                <span className="text-xs mt-1 capitalize">{iconType}</span>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
};

IconSelector.propTypes = {
  selectedIcon: PropTypes.string.isRequired,
  onChange: PropTypes.func.isRequired
};

export default IconSelector;

